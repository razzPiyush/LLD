package Behavioral.Command.Command;

import Behavioral.Command.Devices.Light;

// Concrete Command: TurnOnLightCommand
public class TurnOnLightCommand implements Command {
    private Light light;

    public TurnOnLightCommand(Light light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.turnOn();
    }

    @Override
    public void undo() {
        light.turnOff();
    }
}